// Working with Numbers
let x = Math.floor(Math.random()*100+1);
let y = Math.floor(Math.random()*50+1);

const sumOutput = x + y;
const differenceOutput = x - y;
const productOutput = x * y;
const quotentOutput = x / y;
const remainderOutput = x % y;


console.log(`x = ${x} and y = ${y}`);
console.log(`sumOutput is ${sumOutput} = ${x} + ${y}`);
console.log(`differenceOutput ${differenceOutput} = ${x} - ${y}`);
console.log(`productOutput ${productOutput} = ${x} * ${y}`);
console.log(`quotentOutput ${quotentOutput} = ${x} / ${y}`);
console.log(`remainderOutput ${remainderOutput} = ${x} / ${y}`);
